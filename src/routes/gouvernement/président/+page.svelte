<h1>Président</h1>

<p>Le nom du chef d'Ofnale est nommé le président.<br><br>
    Pour élire un président, les membres du conseil central votent pour le président. Les membres du conseil central peuvent choisir n'importe qui. Pour élire un président, tu dois avoir un deux tiers majorité. Il n'y a pas un terme pour être président. Tu es président jusqu'au conseil central décide de choisir quelqu'un d'autre.<br><br>
    Tous les trois années, il y a une élection pour le conseil central. Le conseil central est divisé en trois conseils plus petits : le conseil d'état, le conseil des habitants et le conseil international. Le conseil d'état dirige le relief. Le conseil des habitants dirige les peuples qui habitent dans Ofnale et le conseil international dirige les relations internationales. Chaque province choisie un membre pour chaque conseil plus petit.<br><br>
    Normalement, chaque parti politique présent son candidat pour chaque conseil dans chaque province.
</p>
